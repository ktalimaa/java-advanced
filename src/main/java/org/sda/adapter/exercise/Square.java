package org.sda.adapter.exercise;

/**
 * Create a class called square that has one field called side, which represents the length of a side of the square.
 *
 * @author Kristel Talimaa
 */

public class Square {

    private int sideLengthOfTheSquare;

    // constructor
    public Square(int sideLengthOfTheSquare) {
        this.sideLengthOfTheSquare = sideLengthOfTheSquare;
    }

    // getter
    public int getSideLengthOfTheSquare() {
        return sideLengthOfTheSquare;
    }
}
